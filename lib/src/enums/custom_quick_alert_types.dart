/// Alert types available in CustomQuickAlert
enum CustomQuickAlertType {
  /// Success alert with green theme
  success,
  /// Error alert with red theme
  error,
  /// Warning alert with orange theme
  warning,
  /// Information alert with blue theme
  info,
  /// Confirmation dialog with action buttons
  confirm,
  /// Loading indicator without action buttons
  loading,
  /// Custom alert with user-defined styling
  custom
}
